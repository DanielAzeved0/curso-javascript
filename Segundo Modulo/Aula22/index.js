/*
Operadores Lógicos
&& = AND = E 
|| = OR = OU
! = NOT = NÃO
*/
const expressaoAnd =  true && true && true && true;
console.log(expressaoAnd);
//todas as expressões precisam ser verdadeiraspara se tornar true 

const expressaoOr = true || false;
console.log(expressaoOr)
//uma das opções tem que ser true para que a opreção retorne true 

console.log(!true);
//o "!" inverte o comando, então oque e true virá false e oque e false virá true 
